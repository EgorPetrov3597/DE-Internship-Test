CREATE TABLE clients (
    id SERIAL PRIMARY KEY,
    name VARCHAR(100),
    is_active BOOLEAN,
    balance NUMERIC(15,2),
    credit_score INTEGER,
    country VARCHAR(50),
    salary NUMERIC(15,2),
    gender VARCHAR(10)
);

CREATE TABLE cards (
    id SERIAL PRIMARY KEY,
    client_id INTEGER REFERENCES clients(id),
    card_type VARCHAR(50),
    churn_date DATE
);

